//math-object in javascript
let num = -5.67;
let x;

x = Math.abs(num);
console.log(x); // 5.67

x = Math.sqrt(16);
console.log(x); // 4

x = Math.ceil(4.2);
console.log(x); // 5

x = Math.floor(4.7);
console.log(x); // 4

x = Math.round(4.5);
console.log(x); // 5

x = Math.max(1, 3, 2, 5, 4);
console.log(x); // 5

x = Math.min(1, 3, 2, 5, 4);
console.log(x); // 1

x = Math.random();
console.log(x); // random number between 0 and 1

x = Math.pow(2, 3);
console.log(x); // 8

x = Math.log(1);
console.log(x); // 0

x = Math.random() * 100; // random number between 0 and 100

x = Math.floor(Math.random() * 100) + 1; // random integer between 1 and 100
console.log(x);

